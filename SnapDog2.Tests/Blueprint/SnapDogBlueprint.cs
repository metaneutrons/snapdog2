        .Command("VOLUME")
        .Zone()
        .Mqtt()
        .Put("/zones/{id}/volume")
        .Description("Set zone volume to specific level")
        .Exclude(Protocol.Knx, "Handled by dedicated KNX volume actuators")
        .Command("VOLUME_UP")
        .Zone()
        .Mqtt()
        .Post("/zones/{id}/volume/up")
        .Description("Increase zone volume")
        .Exclude(Protocol.Knx, "Handled by dedicated KNX volume actuators")

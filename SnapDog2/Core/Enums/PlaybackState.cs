namespace SnapDog2.Core.Enums;

/// <summary>
/// Represents the playback state of a zone.
/// </summary>
public enum PlaybackState
{
    /// <summary>
    /// Playback is stopped.
    /// </summary>
    Stopped,

    /// <summary>
    /// Playback is currently playing.
    /// </summary>
    Playing,

    /// <summary>
    /// Playback is paused.
    /// </summary>
    Paused,
}
